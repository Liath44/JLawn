public class Sprinkler90 extends Sprinkler
	{
	private final static int type = 90;
	private final static int code = 0;
	
	public int getCode()
		{
		return code;
		}
		
	public int getType()
		{
		return type;
		}

	public int getRadius()
		{
		return radius;
		}
		
	public Sprinkler90(int x, int y, int deg)
		{
		super(x, y, deg);
		}
	}
